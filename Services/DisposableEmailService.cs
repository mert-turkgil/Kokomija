        private readonly ILogger<DisposableEmailService> _logger;

        public DisposableEmailService(ILogger<DisposableEmailService> logger)
        {
            _logger = logger;
        }

        public bool IsDisposable(string email)
        {
            if (string.IsNullOrWhiteSpace(email))
                return false;

            var atIndex = email.LastIndexOf('@');
            if (atIndex < 0 || atIndex >= email.Length - 1)
                return false;

            var domain = email[(atIndex + 1)..].Trim().ToLowerInvariant();

            if (DisposableDomains.Contains(domain))
            {
                _logger.LogWarning("Blocked disposable email domain: {Domain} (email: {Email})", domain, email);
                return true;
            }

            return false;
        }
